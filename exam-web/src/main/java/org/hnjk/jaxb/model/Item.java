package org.hnjk.jaxb.model;

import java.util.List;

import javax.xml.bind.annotation.XmlAttribute;
/**
 * 课件模块的子项
 * @author zxh
 *
 */
public class Item {
	/**
	 * 课件节点名称
	 */
	String title;
	/**
	 * 是否可见
	 */
	String isvisible="true";
	/**
	 * 课件架构节点ID
	 */
	String identifier;
	/**
	 * 课件架构引用资源的ID
	 */
	String identifierref;
	
	/**是否显示New的标识*/
	private java.lang.String isNew;
	/**New的标识过期时间*/
	private java.lang.String expirationDate;
	/**资源大小*/
	private java.lang.String resourceSize;
	/**视频总时长*/
	private java.lang.String videoTotalTime;
	/**
	 * 子节点
	 */
	List<Item> item;
	/**完成情况   0：未开始 1：进行中 2：已完成*/
	String finishStatus;
	/**完成百分比或分数*/
	String finishPercent;
	
	/**显示须知*/
	private java.lang.String showNotice;
	/**显示进度*/
	private java.lang.String showSchedule;
	/**显示评价*/
	private java.lang.String showEvaluate;
	/**显示提问*/
	private java.lang.String showAsk;
	/**显示分享*/
	private java.lang.String showShare;
	
	public String getIsvisible() {
		return isvisible;
	}
	@XmlAttribute(required = true)
	public void setIsvisible(String isvisible) {
		this.isvisible = isvisible;
	}
	public String getIdentifier() {
		return identifier;
	}
	@XmlAttribute(required = true)
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	public String getIdentifierref() {
		return identifierref;
	}
	@XmlAttribute(required = true)
	public void setIdentifierref(String identifierref) {
		this.identifierref = identifierref;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public List<Item> getItem() {
		return item;
	}
	public void setItem(List<Item> item) {
		this.item = item;
	}
	public java.lang.String getIsNew() {
		return isNew;
	}
	public java.lang.String getExpirationDate() {
		return expirationDate;
	}
	@XmlAttribute(required = true)
	public void setIsNew(java.lang.String isNew) {
		this.isNew = isNew;
	}
	@XmlAttribute(required = true)
	public void setExpirationDate(java.lang.String expirationDate) {
		this.expirationDate = expirationDate;
	}
	public java.lang.String getResourceSize() {
		return resourceSize;
	}
	public java.lang.String getVideoTotalTime() {
		return videoTotalTime;
	}
	@XmlAttribute(required = true)
	public void setResourceSize(java.lang.String resourceSize) {
		this.resourceSize = resourceSize;
	}
	@XmlAttribute(required = true)
	public void setVideoTotalTime(java.lang.String videoTotalTime) {
		this.videoTotalTime = videoTotalTime;
	}
	public String getFinishStatus() {
		return finishStatus;
	}
	public void setFinishStatus(String finishStatus) {
		this.finishStatus = finishStatus;
	}
	public String getFinishPercent() {
		return finishPercent;
	}
	public void setFinishPercent(String finishPercent) {
		this.finishPercent = finishPercent;
	}
	
	public java.lang.String getShowNotice() {
		return showNotice;
	}
	public java.lang.String getShowSchedule() {
		return showSchedule;
	}
	public java.lang.String getShowEvaluate() {
		return showEvaluate;
	}
	public java.lang.String getShowAsk() {
		return showAsk;
	}
	public java.lang.String getShowShare() {
		return showShare;
	}
	@XmlAttribute(required = true)
	public void setShowNotice(java.lang.String showNotice) {
		this.showNotice = showNotice;
	}
	@XmlAttribute(required = true)
	public void setShowSchedule(java.lang.String showSchedule) {
		this.showSchedule = showSchedule;
	}
	@XmlAttribute(required = true)
	public void setShowEvaluate(java.lang.String showEvaluate) {
		this.showEvaluate = showEvaluate;
	}
	@XmlAttribute(required = true)
	public void setShowAsk(java.lang.String showAsk) {
		this.showAsk = showAsk;
	}
	@XmlAttribute(required = true)
	public void setShowShare(java.lang.String showShare) {
		this.showShare = showShare;
	}
}
