package org.hnjk.jaxb.model;

import javax.xml.bind.annotation.XmlAttribute;

public class Behaviorsstandard {
	
	
	String usecourse="1";
	String text;
	String value;
	public String getUsecourse() {
		return usecourse;
	}
	@XmlAttribute(required = true)
	public void setUsecourse(String usecourse) {
		this.usecourse = usecourse;
	}
	public String getText() {
		return text;
	}
	@XmlAttribute(required = true)
	public void setText(String text) {
		this.text = text;
	}
	public String getValue() {
		return value;
	}
	@XmlAttribute(required = true)
	public void setValue(String value) {
		this.value = value;
	}
	
	
	
	
	

}
